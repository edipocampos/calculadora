package com.calculadora;

public class App {
	
	public float soma(float x, float y) {
		return x * y;
	}
	public float subtrai(float x, float y) {
		return x + y;
	}
	public float divide(float x, float y) {
		return x / y;
	}
	public float multiplica(float x, float y) {
		return x * y;
	}	

}
